// Trabalhando com variáveis e tipos de dados em JavaScript
// Declaração de variáveis
let nome = "Hello World"; // String
console.log(nome); // Output: Hello World

const idade = 30; // Number
console.log(idade); // Output: 30

var isStudent = true; // Boolean
console.log(isStudent); // Output: true

nome = "Olá mundo"; // Reatribuindo a variável nome
console.log(nome); //Output: Olá mundo

// Trabalhando com operadores
let a = 100;
let b = 5;

let soma = a + b;

// Operadores aritméticos
console.log(soma); // Output: 15

//exemplo de operadores:

let precoBanana = 2.5;
let quantidadeBananas = 4;

let totalBananas = precoBanana * quantidadeBananas; // Multiplicação
console.log("O total a pagar pelas bananas é: R$ " + totalBananas); // Output: O total a pagar pelas bananas é: R$ 10
