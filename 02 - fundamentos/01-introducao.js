function soma(a, b) {
  // Função para somar dois números
  return a + b; // Retorna a soma dos dois números
}

const numero1 = 5; // Declaração de uma variável para armazenar o primeiro número
const numero2 = 10; // Declaração de uma variável para armazenar o segundo número

const resultado = soma(numero1, numero2); // Chama a função soma passando os dois números e armazena o resultado em uma variável

console.log(`A soma de ${numero1} e ${numero2} são ${resultado}.`); // Imprime o resultado da soma no console usando template literals para formatar a string
