// Variáveis

// let é uma palavra reservada para criar uma variável
// ou seja, um espaço na memória do computador para armazenar um valor.

let animal = "Cachorro"; // declaração de uma variável chamada 'animal' e atribuição do valor 'cachorro'
console.log(animal); // imprime o valor da variável 'animal' no console

animal = "Gato"; // reatribuição do valor da variável 'animal' para 'gato'
console.log(animal); // imprime o novo valor da variável 'animal' no console

// const é uma palavra reservada para criar uma variável constante
// ou seja, um espaço na memória do computador para armazenar um valor que não pode ser alterado.
// armazenar um valor que não pode ser alterado, ou seja, uma constante.

const pi = 3.14; // declaração de uma constante chamada 'pi' e atribuição do valor 3.14
console.log(pi); // imprime o valor da constante 'pi' no console

pi = 3.14159;
console.log(pi); // tenta atribuir um novo valor à constante 'pi', o que resultará em um erro, pois constantes não podem ser reatribuídas
