// Funções de seta (Arrow Functions) são uma forma mais concisa
// de escrever funções em JavaScript. Elas foram introduzidas
// no ECMAScript 6 (ES6) e tem uma sintaxe mais curta em comparação
// com as funções tradicionais.

// Exemplo 1: Função de seta simples
const saudacao = () => {
  return "Olá mundo";
};

console.log(saudacao());

// Exemplo 2:
const soma = (a, b) => {
  return a + b;
};

console.log(soma(89, 1));

// Exemplo 3:
const mutiplicacao = (a, b) => a * b;

console.log(mutiplicacao(2, 3));
